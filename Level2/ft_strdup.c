#include <stdlib.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}

char   	*ft_strdup(char *src)
{
	char	*new;
	int		len;
	int		i;

	i = 0;
	len = ft_strlen(src);
	new = (char *)malloc(len + 1);
	if (new == 0)
		return (0);
	while (src[i] != '\0')
	{
		new[i] = src[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}

